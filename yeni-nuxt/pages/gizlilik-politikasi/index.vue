              <h3>viii. Gizlilik</h3>

              Kişisel veriler gizliliğe tabidir. Çalışanların verileri izinsiz olarak toplaması, işlemesi ya da
              kullanması yasaktır. Yetkisiz kullanım, çalışanların meşru görevleri dışında gerçekleştirdikleri yetkisiz
              veri işlemesidir. Bil prensibi geçerlidir: Çalışanlar kişisel verilere sadece söz konusu görevin kapsamı
              ve mahiyetine uygun olması halinde erişebilirler.

              Çalışanların kişisel verileri özel ya da ticari amaçlar için kullanması, yetkisiz kişilere dağıtması ya da
              başka bir şekilde erişilebilir kılması yasaklanmıştır. Yöneticiler çalışanlarını iş ilişkisinin başladığı
              sırada veri koruma ile ilgili yükümlülükler hakkında bilgilendirmelidir. Bu yükümlülük, iş ilişkisinin
              sonlandırılmasından sonra da devam eder.

              <h3>ix. Güvenlik</h3>

              TIAC işlemekte olduğu kişisel verilerin hukuka aykırı olarak işlenmesini önlemek, verilere hukuka aykırı
              olarak erişilmesini önlemek ve verilerin muhafazasnı sağlamak için uygun güvenlik düzeyini sağlamaya
              yönelik gerekli tedbir ve kontrolleri almakta, bu kapsamda gerekli denetimleri yapmakta veya
              yaptırmaktadır. Bu husus, veri işlemenin elektronik yolla veya yazılı olarak yapılmasından bağımsız olarak
              geçerlidir. Özellikle yeni BT sistemlerine geçişte veri işlemenin yeni metotlarına başlamadan önce,
              kişisel verilerin korunmasına yönelik teknik ve organizasyonel önlemler tanımlanmaktadır ve
              uygulanmaktadır. Bu önlemler son gelişmelere, işlemin risklerine ve bilgi sınıflandırması süreci ile
              belirlenen, verinin koruma ihtiyacına dayandırılmıştır. Kişisel verilerin korunmasına ilişkin teknik ve
              organizasyonel önlemler, şirket bilgi güvenliği yönetiminin bir parçasıdır ve teknik gelişmelere ve
              organizasyonel değişikliklere sürekli olarak uyarlanmaktadır.

              <h3>x. Kontrol ve Denetimler</h3>

              Kişisel Verilerin Korunması ve İşlenmesi Politikasına ve KVKK’ya uygunluk, düzenli veri koruma denetimleri
              ve diğer kontrollerle sağlanmaktadır.

              <h3>xi. Veri İhlalleri Yönetimi</h3>

              TIAC, bu Politika ve KVKK hükümlerine aykırı olarak ele geçirilen kişisel verilerin korunması için gerekli
              güvenlik tedbirlerini ivedilikle yerine getirecek ve bu durumu en kısa sürede ilgili kişiye ve Kurul’a
              bildirecektir. Bu amaçla kişisel veri sahiplerinin, kişisel verilerine ilişkin talep ve şikayetlerini
              kendisine en efektif ve kısa süre içinde iletilmesini sağlayan sistem ve başvuru yöntemlerinin
              oluşturulması TIAC’ın sorumluluğundadır. Kurul tarafından gerek görülmesi halinde, bu durum, Kurul’un
              internet sitesinde veya başka bir yöntemle ilan edilebilecektir.

              <h3>xii. Veri Sorumluları Siciline Kaydolma Yükümlülüğü</h3>

              TIAC, veri sorumluları siciline kayıt yükümlüsü haline gelmesi halinde, yükümlülük altına girdiği tarihten
              itibaren otuz gün içerisinde KVKK’da sayılan başvuru bilgi ve belgelerini sunarak Veri Sorumluları
              Sicili’ne kayıt olacaktır.

