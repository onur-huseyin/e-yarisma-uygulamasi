            <h3>ii. Amaç</h3>

            Bu Politika; This Is A Company Teknoloji Gelişim ve Yatırım A.Ş.’nin (“TIAC”) 7 Nisan 2016 tarihli Resmi
            Gazete’de yayınlanarak yürürlüğe giren 6698 Sayılı Kişisel Verilerin Korunması Kanunu (“KVKK”) kapsamında
            veri sorumlularına getirilen yükümlülüklere uyumun sağlanmasında benimsenecek temel ilkeler ve uygulama
            prensiplerinin belirlenmesi amacıyla oluşturulmuştur.

            <h3>iii. Kapsam ve Değişikler</h3>

            KVKK’ya uygun olarak hazırlanan bu Politika mevcut ve potansiyel müşteri ve çalışanlarımız ile işbirliği
            içinde olduğumuz kurumların çalışanları, hissedarları ve yetkililerinin ve üçüncü şahısların otomatik olan
            ya da herhangi bir veri kayıt sisteminin parçası olmak kaydıyla otomatik olmayan yollarla işlenen tüm
            kişisel verilerine ilişkindir. TIAC’ın KVKK ve ilgili yönetmelikte yapılacak değişikler doğrultusunda,
            Protokol’de değişiklik yapma hakkı saklıdır.

            <h3>iv. Kişisel Verilerin İşlenmesinde Uygulanacak İlkeler</h3>

            TIAC, kişisel verilen toplanması, işlenmesi ve analiz edilmesinde aşağıdaki ilkeleri benimsemiştir.

            <h3>a. Hukuka ve Dürüstlük Kurallarına Uygun Faaliyette Bulunma</h3>
            TIAC veri sahiplerinin haklarının korunması için kişisel verileri hukuka uygun ve adil bir şekilde
            toplayarak, işleyecektir. Bu faaliyetlerin yürütülmesinde orantılılık ve gereklilik ilkeleri göz önünde
            bulundurulacaktır.

            <h3>b. Amaca Özel Kısıtlama</h3>
            Kişisel veri yalnızca verilerin toplanmasından önce tanımlanmış amaçlar için işlenebilir. Amaca ilave
            değişimler yalnızca sınırlı ölçüde ve gerekçelendirmeyle mümkündür.

            <h3>c. Şeffaflık ve Aydınlatma</h3>
            Veri sahipleri, kişisel verilerinin toplanması ve işlenmesi öncesinde detaylı olarak bilgilendirilmelidir.
            Verilerin toplanması öncesinde hak sahipleri aşağıdaki hususlarda bilgilendirilmelidir:
            <ul class="disc">
              <li>Veri sorumlusunun ve varsa temsilcisinin kimliği</li>
              <li>Kişisel verilerin işlenme amacı</li>
              <li>İşlenen kişisel verilerin kimlere ve hangi amaçla aktarıldığı</li>
              <li>Kişisel veri toplamanın yöntemi ve hukuki sebebi,</li>
              <li>Kişisel verisi işlenen kişinin KVKK madde 11 uyarınca hakları.</li>
            </ul>

            <h3>d. Veri Ekonomisi</h3>
            Kişisel verinin işlenmesinden önce, amaca ulaşmak için işlemin gerekli olup olmadığı ve hangi kapsamda
            gerekli olduğu belirlenmelidir. Amacın kabul edilebilir ve orantılı olduğu durumda anonim ya da istatistiki
            veri kullanılabilir.

            <h3>e. Kişisel Verilerin Silinmesi</h3>
            İlgili kanunlarda kayıt saklama yükümlülükleri ve ispat için gerekli kayıt tutma işlemleri için öngörülen
            sürelerin sona ermesinden sonra artık gerekli olmayan kişisel veri silinmekte veya yok edilmekte veya anonim
            hale getirilmektedir.

            <h3>f. Doğruluk ve Veri Güncelliği</h3>
            Kişisel verilerin doğru, tam ve biliniyor olması halinde güncel olması zorunludur. Doğru
            olmayan veya eksik olan verilerin silinmesi, düzeltilmesi, tamamlanması veya güncellenmesi sağlanmalıdır.

            <h3>g. Gizlilik ve veri güvenliği</h3>
            Kişisel veri gizli bilgi olarak saklanmalı ve muhafaza edilmelidir. Kişisel Veriler yetkisiz erişimi, yasal
            olmayan işlemleri, paylaşımı, yanlışlıkla kaybolmayı, değiştirilmeyi veya tahrip edilmeyi engellemek için
            gerekli idari ve teknik tedbirler alınarak korunmalı ve kişisel seviyede gizli tutulmaktadır.

