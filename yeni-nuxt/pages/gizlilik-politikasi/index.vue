              <h3>vi. Kişisel Verilerin Aktarılması </h3>

              Kişisel verilerin TIAC dışındaki bir üçüncü kişiye aktarılması Aydınlatma Metni’nde yer alan ve aşağıda
              belirtilen amaçlar kapsamında gerçekleştirilecektir. Buna göre TIAC kişisel verileri aşağıda belirtilen
              kişi ve kurumlara belirli amaçlarla aktarabilecektir;
              <ul>
                <li>İş ortaklığının kurulma amaçlarının yerine getirilmesini temin etmek amacıyla sınırlı olarak TIAC iş
                  ortaklarına,</li>
                <li>TIAC’ın tedarikçiden dış kaynaklı olarak temin ettiği ve ticari faaliyetlerini yerine getirebilmesi
                  için gerekli ürün ve hizmetleri sunan tedarikçilerine,</li>
                <li>TIAC iştiraklerinin de katılımını gerektiren ticari faaliyetlerinin yürütülmesini temin etmekle
                  sınırlı olarak TIAC iştiraklerine,</li>
                <li>KVKK hükümlerine uygun TIAC’ın ticari faaliyetlerine ilişkin stratejilerin tasarlanması ve denetim
                  amaçlarıyla sınırlı olarak TIAC hissedarlarına,</li>
                <li>İlgili kamu kurum ve kuruluşlarının hukuki yetkisi dâhilinde talep ettiği amaçla sınırlı olarak
                  hukuken yetkili kamu kurum ve kuruluşlarına,</li>
                <li>İlgili özel hukuk kişilerinin hukuki yetkisi dâhilinde talep ettiği amaçla sınırlı olarak hukuken
                  yetkili özel hukuk kişilerine.</li>
              </ul>
              TIAC tarafından işlenen kişisel verileriniz Kurul tarafından yeterli korumaya sahip yabancı ülkeler ilan
              edildikten sonra bu ülkelere aktarılacaktır. Yeterli korumanın bulunmadığı ilan edilen ülke ve bölgelere
              ise kişisel veriler ancak, veri sahibinin onay verdiği durumda veya Türkiye’deki ve ilgili yabancı
              ülkedeki veri sorumlularının yeterli bir korumayı yazılı olarak taahhüt ettiği ve Kurulun izninin
              bulunduğu durumlarda aktarılabilecektir. TIAC, kişisel verilerinizin işlenmesinde bulut depolama hizmeti
              de kullanabilir.

