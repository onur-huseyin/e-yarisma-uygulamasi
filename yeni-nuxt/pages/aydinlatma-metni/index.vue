            <h3 class="text-xl font-semibold text-gray-600 mt-10 pb-2">ÇEREZLER (COOKİELER)</h3>

            Kullanıcıların tercihlerinin izlenmesi amacıyla internet sitemizde cookieler kullanılmaktadır. Cookieler,
            bilgisayarınızın sabit diskine bırakılan küçük dosyalardır. Bu, navigasyonu kolaylaştırır ve internet
            sitesinin çok daha rahat kullanılmasını sağlar. Cookieler aynı zamanda bizim internet sitemizde en popüler
            olan alanları saptamamıza yardımcı olmaktadır. Böylelikle internet sitemizin içeriklerini sizin
            gereksinimlerinizi daha iyi karşılayacak biçimde oluşturabilir, dolayısıyla size sunduğumuz hizmeti de
            iyileştirebiliriz. Cookieler; sizin bilgisayarınızdan daha önce internet sitemize erişilip erişilmediğini
            saptamak için de kullanılabilir.

            Internet sitemizi cookieler olmaksızın da kullanabilirsiniz Cookielerin bilgisayarınızın sabit diskine
            kaydedilmesini, browser ayarlarında *cookie engelle* ayarını seçerek engelleyebilirsiniz. Ancak birçok
            browserın cookieleri otomatik olarak kabul ettiği ve cookie kabul etmemeniz halinde internet sitemizi
            kullanımınızın sınırlanabileceğini hususunda dikkatinizi çekmek isteriz.


